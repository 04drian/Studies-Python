# Programa que leia um número inteiro e mostre seu antecessor e seu sucessor

    # Recebimento das informações

numero_1 = int(input('Digite um número inteiro: '))

    # Apuração

antecessor =  numero_1 - 1

sucessor = numero_1 + 1

    # Resultado

print(f'O número {numero_1} tem como seu antecessor o {antecessor} e seu sucessor {sucessor}')